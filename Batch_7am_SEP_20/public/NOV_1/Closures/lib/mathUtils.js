

function getAddition(a, b) {
	var result = 0;
	result = a + b;
	return result;
}

function getMultiplication(a, b) {
	var result = 0;
	result = a * b;
	return result;
}

function getSubstraction(a, b) {
	var result = 0;
	result = a - b;
	return result;
}

function getDivision(a, b) {
	var result = 0;
	result = a / b;
	return result;
}